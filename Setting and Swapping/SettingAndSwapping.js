//Basic Swapping for Algorithms

// Setting and Swapping
// Set variable myNumber to 42. Set variable myName to your name. Now swap myNumber into myName & vice versa.

var myNumber = 42;
var myName = 'carlos';
var temp = myNumber;
myNumber = myName;
myName = temp;

console.log("myNumber now is",myNumber);
console.log("myName now is", myName);
